/**
 * filter
 * 배열안의 요소를 반복하며 조건에 맞는 요소들 true인 것만 뽑아내서 다른 배열로 반환함.
 * 항상 return 값이 항상 배열임.
 * 하나의 값만 찾고 싶다면, filter가 아닌 find메서드를 사용해야됨
 * 
 * find
 * 배열안의 요소를 반복하며 조건에 맞는 요소 하나를 뽑아냄
 * 조건을 충족하는 순간 반복을 멈춤.
 * 때문에 filter와 반복횟수가 달라질 수 있음. 메모리 효율 측면에서 생각했을 때 filter와 다른점이니 기억해두자.
 * 그리고 배열로 반환하는게 아니고 값을 반환함.
 */